import AsyncStorage from "@react-native-async-storage/async-storage";

// ── Profile types ──────────────────────────────────────────────────────────────

export type Sex             = "male" | "female";
export type AgeGroup        = "13-18" | "19-30" | "31-50" | "51-70" | "71+";
export type PregnancyStatus = "none" | "pregnant" | "lactating";

export interface UserProfile {
  sex:      Sex;
  age:      AgeGroup;
  pregnancy: PregnancyStatus;
}

export const AGE_GROUPS: AgeGroup[] = ["13-18", "19-30", "31-50", "51-70", "71+"];

export const PROFILE_STORAGE_KEY = "nutrirank_user_profile_v2";

export const DEFAULT_PROFILE: UserProfile = {
  sex:      "male",
  age:      "19-30",
  pregnancy: "none",
};

export function getProfileLabel(p: UserProfile): string {
  const age = `(${p.age})`;
  if (p.sex === "female" && p.pregnancy === "pregnant")  return `Pregnant ${age}`;
  if (p.sex === "female" && p.pregnancy === "lactating") return `Lactating ${age}`;
  return `${p.sex === "male" ? "Male" : "Female"} ${age}`;
}

/** Returns true for age groups where pregnancy/lactation is physiologically applicable. */
export function pregnancyAllowed(age: AgeGroup): boolean {
  return age === "13-18" || age === "19-30" || age === "31-50";
}

// ── AsyncStorage ───────────────────────────────────────────────────────────────

export async function loadProfile(): Promise<UserProfile> {
  try {
    const raw = await AsyncStorage.getItem(PROFILE_STORAGE_KEY);
    if (!raw) return DEFAULT_PROFILE;
    const p = JSON.parse(raw) as Partial<UserProfile>;
    const sexOk  = p.sex === "male" || p.sex === "female";
    const ageOk  = AGE_GROUPS.includes(p.age as AgeGroup);
    const pregOk = p.pregnancy === "none" || p.pregnancy === "pregnant" || p.pregnancy === "lactating";
    if (!sexOk || !ageOk || !pregOk) return DEFAULT_PROFILE;
    const profile: UserProfile = { sex: p.sex!, age: p.age!, pregnancy: p.pregnancy! };
    if (!pregnancyAllowed(profile.age) || profile.sex === "male") profile.pregnancy = "none";
    return profile;
  } catch {
    return DEFAULT_PROFILE;
  }
}

export async function saveProfile(profile: UserProfile): Promise<void> {
  await AsyncStorage.setItem(PROFILE_STORAGE_KEY, JSON.stringify(profile));
}

